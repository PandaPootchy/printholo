local function load()
	if filesystem.exists(setfile) then
		local f = io.open(setfile, "r")
		local holoColor = f:read('*l')
		local holoScale = f:read('*l')
		changeHoloColor(tonumber(holoColor))
		hologram.setScale(tonumber(holoScale))
		f:close()
	else
		print(setfile .. " not found creating..")
		local f = io.open(setfile, "w")
		f:write(holoColor, "\n")
		f:write(holoScale)
		f:close()
	end
end

local function save(hC, hS)
		local f = io.open(setfile, "w")
		f:write(hC, "\n")
		f:write(hS)
		f:close()
end


