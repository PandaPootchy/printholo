local function drawSymbolOnProjector(x, y, z, symbol)
	local xPos = x
	for j = 1, #symbols[symbol] do
		for i = 1, #symbols[symbol][j] do
			if symbols[symbol][j][i] == 1 then
				hologram.set(xPos, y, z, 1)
			else
				hologram.set(xPos, y, z, 0)
			end
			xPos = xPos + 1
		end
		xPos = x
		y = y - 1
	end
end

--figure out lines and caculate where said lines and symbols will go (complete)
local function drawText(x, y, text)
	for i = 1, unicode.len(text) do
		local symbol = unicode.sub(text, i, i)
		drawSymbolOnProjector( x, y, 24, symbol)
		--print(i .. " " .. symbol .. " " .. x .. " " .. y)
		x = x + 6
		if x == 49 then
			y = y - 8
			x = x - 48
			--print(" ")
		end
	end
end

local function changeHoloColor(color)
	hologram.setPaletteColor(1, color)
end



local function draw(number)

